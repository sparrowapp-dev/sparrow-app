<script lang="ts">
  import type {
    KeyValuePair,
    NewTab,
  } from "$lib/utils/interfaces/request.interface";
  import KeyValue from "$lib/components/key-value/KeyValue.svelte";
  import { createDeepCopy } from "$lib/utils/helpers/conversion.helper";
  import AngleUp from "$lib/assets/angle-up.svelte";
  import AngleDown from "$lib/assets/angle-down2.svelte";
  import { onMount } from "svelte";
  import {
    findAuthHeader,
    setContentTypeHeader,
  } from "$lib/utils/helpers/auth.helper";
  import {
    RequestDataset,
    RequestProperty,
  } from "$lib/utils/enums/request.enum";
  import type { CollectionsMethods } from "$lib/utils/interfaces/collections.interface";
  export let request;
  export let contentType;
  export let collectionsMethods: CollectionsMethods;
  export let currentTabId;
  export let environmentVariables;

  let authValue: { key: string; value: string } = {
    key: "",
    value: "",
  };

  let showGeneratedHeader: boolean = false;

  $: {
    if (request) {
      authValue = findAuthHeader(request);
    }
  }

  onMount(() => {
    authValue = findAuthHeader(request);
    const contentTypeIndex = request.autoGeneratedHeaders.findIndex(
      (header: KeyValuePair) => header.key === "Content-Type",
    );
    if (request && request.state.dataset !== RequestDataset.NONE) {
      const bodyType =
        request.state.dataset === RequestDataset.RAW
          ? request.state.raw
          : request.state.dataset;
      contentType = setContentTypeHeader(bodyType);
      const contentTypeHeader: KeyValuePair = {
        key: "Content-Type",
        value: contentType,
        checked: true,
      };
      contentTypeIndex !== -1
        ? (request.autoGeneratedHeaders[contentTypeIndex] = contentTypeHeader)
        : request.autoGeneratedHeaders.push(contentTypeHeader);
    } else if (contentTypeIndex !== -1) {
      request.autoGeneratedHeaders.splice(contentTypeIndex, 1);
    }
  });

  const handleHeaderChange = (pairs: KeyValuePair[]): void => {
    collectionsMethods.updateRequestProperty(
      pairs,
      RequestProperty.HEADERS,
      currentTabId,
    );
  };

  const handleAutoGeneratedChange = (pairs: KeyValuePair[]): void => {
    collectionsMethods.updateRequestProperty(
      pairs,
      RequestProperty.AUTO_GENERATED_HEADERS,
      currentTabId,
    );
  };
</script>

<section class="w-100">
  <KeyValue
    keyValue={createDeepCopy(request.headers)}
    callback={handleHeaderChange}
    {environmentVariables}
  />
  {#if !showGeneratedHeader}
    <div>
      <small
        on:click={() => {
          showGeneratedHeader = true;
        }}
        class="text-labelColor generated-para"
        >Show auto-generated headers <AngleDown
          height={16}
          width={16}
          color={"var(--send-button)"}
        /></small
      >
    </div>
  {/if}
  {#if showGeneratedHeader}
    <div>
      <small
        on:click={() => {
          showGeneratedHeader = false;
        }}
        class="text-labelColor generated-para"
        >Hide auto-generated headers <AngleUp
          height={16}
          width={16}
          color={"var(--send-button)"}
        />
      </small>
    </div>
    <hr class="mt-1" />
    <KeyValue
      mode={"READ"}
      readable={authValue}
      keyValue={createDeepCopy(request.autoGeneratedHeaders)}
      callback={handleAutoGeneratedChange}
      {environmentVariables}
    />
  {/if}
</section>

<style>
  .generated-para {
    font-size: 12px;
    cursor: pointer;
  }
</style>
