<script lang="ts">
  import { Loader } from "@sparrow/library/ui";
  import { Modal } from "@sparrow/library/ui";
  import { Splitpanes, Pane } from "svelte-splitpanes";

  import type { CollectionDocument } from "@app/database/database";
  import type { Observable } from "rxjs";
  import { SaveAsCollectionItem } from "@sparrow/workspaces/features";

  import type {
    CreateCollectionType,
    CreateFolderType,
    ReadWorkspaceType,
    SaveAsRequestType,
    SaveRequestType,
    SendRequestType,
    UpdateAutoGeneratedHeadersType,
    UpdateHeadersType,
    UpdateParamsType,
    UpdateRequestUrlType,
  } from "@sparrow/workspaces/type";
  import { type Tab } from "@sparrow/common/types/workspace/tab";
  import { TabTypeEnum } from "@sparrow/common/types/workspace/tab";

  import {
    HttpUrlSection,
    ResponseDefaultScreen,
    ResponsePreview,
    ResponsePreviewNavigator,
    SocketHeaders,
    SocketMessage,
    SocketNavigator,
    SocketParameters,
  } from "../components";
  import { SocketSectionEnum } from "@sparrow/common/types/workspace/web-socket";
  import ResponseData from "../components/response-data/ResponseData.svelte";

  export let tab: Observable<Tab>;
  export let collections: Observable<CollectionDocument[]>;
  export let onUpdateRequestUrl: UpdateRequestUrlType;
  export let onUpdateRequestParams: UpdateParamsType;
  export let onUpdateMessage;
  export let onUpdateHeaders: UpdateHeadersType;
  export let onUpdateAutoGeneratedHeaders: UpdateAutoGeneratedHeadersType;
  export let onUpdateRequestState;
  export let onSaveSocket: SaveRequestType;
  export let readWorkspace: ReadWorkspaceType;
  export let onSaveAsSocket: SaveAsRequestType;
  export let onCreateFolder: CreateFolderType;
  export let onCreateCollection: CreateCollectionType;
  export let onUpdateEnvironment;
  export let environmentVariables;
  export let isGuestUser = false;
  /**
   * Role of user in active workspace
   */
  export let userRole: string;
  export let onRenameCollection;
  export let onRenameFolder;
  export let onConnect;
  export let onSendMessage;
  export let webSocket;
  export let onDisconnect;
  export let onSearchMessage;
  export let onDeleteMessage;
  export let onUpdateMessageBody;
  export let onUpdateContentType;
  export let onClearInput;
  export let onUpdateFilterType;
  export let isWebApp = false;
  let isExposeSaveAsSocket = false;
  const toggleSaveRequest = (flag: boolean): void => {
    isExposeSaveAsSocket = flag;
  };
</script>

{#if $tab.tabId}
  <div class="d-flex rest-explorer-layout h-100">
    <div class="w-100 d-flex flex-column h-100 px-3 pt-3 pb-2">
      <!-- HTTP URL Section -->
      <HttpUrlSection
        class=""
        isSave={$tab.isSaved}
        bind:userRole
        requestUrl={$tab.property.websocket?.url}
        {onUpdateEnvironment}
        {environmentVariables}
        {onUpdateRequestUrl}
        {toggleSaveRequest}
        {onSaveSocket}
        {isGuestUser}
        {onConnect}
        {webSocket}
        {onDisconnect}
      />

      <div class="pt-2"></div>
      <div style="flex:1; overflow:auto;">
        <!-- {#if !isLoading} -->
        <Splitpanes
          class="rest-splitter w-100 h-100"
          id={"rest-splitter"}
          horizontal={false}
          dblClickSplitter={false}
          on:resize={(e) => {
            onUpdateRequestState({
              socketLeftSplitterWidthPercentage: e.detail[0].size,
            });
            onUpdateRequestState({
              socketRightSplitterWidthPercentage: e.detail[1].size,
            });
          }}
        >
          <Pane
            minSize={30}
            size={$tab.property.websocket?.state
              ?.socketLeftSplitterWidthPercentage}
            class="position-relative bg-secondary-850-important"
          >
            <!-- Request Pane -->
            <div class="h-100 d-flex flex-column position-relative pe-2">
              <SocketNavigator
                requestStateSection={$tab.property.websocket?.state
                  ?.socketNavigation}
                {onUpdateRequestState}
                paramsLength={$tab.property?.websocket?.queryParams?.length ||
                  0}
                headersLength={$tab.property?.websocket?.headers?.length || 0}
                autoGeneratedHeadersLength={$tab.property?.websocket
                  ?.autoGeneratedHeaders?.length || 0}
              />
              <div style="flex:1; overflow:auto;" class="p-0">
                {#if $tab.property.websocket?.state?.socketNavigation === SocketSectionEnum.MESSAGE}
                  <SocketMessage
                    body={$tab?.property?.websocket?.message}
                    requestState={$tab?.property?.websocket?.state}
                    {onUpdateMessage}
                    {onUpdateRequestState}
                    {onSendMessage}
                    {onClearInput}
                    {webSocket}
                  />
                {:else if $tab.property.websocket?.state?.socketNavigation === SocketSectionEnum.PARAMETERS}
                  <SocketParameters
                    isBulkEditActive={$tab?.property?.websocket.state
                      ?.isParameterBulkEditActive}
                    {onUpdateRequestState}
                    params={$tab.property.websocket.queryParams}
                    {onUpdateRequestParams}
                    {onUpdateEnvironment}
                    {environmentVariables}
                  />
                {:else if $tab.property.websocket?.state?.socketNavigation === SocketSectionEnum.HEADERS}
                  <SocketHeaders
                    isBulkEditActive={$tab?.property?.websocket.state
                      ?.isHeaderBulkEditActive}
                    {onUpdateRequestState}
                    {environmentVariables}
                    {onUpdateEnvironment}
                    headers={$tab.property.websocket.headers}
                    autoGeneratedHeaders={$tab.property.websocket
                      .autoGeneratedHeaders}
                    onHeadersChange={onUpdateHeaders}
                    onAutoGeneratedHeadersChange={onUpdateAutoGeneratedHeaders}
                  />
                {/if}
              </div>
            </div>
          </Pane>
          <Pane
            minSize={30}
            size={$tab.property.websocket?.state
              ?.socketRightSplitterWidthPercentage}
            class="bg-secondary-850-important position-relative"
          >
            <!-- Response Pane -->
            <div class="d-flex flex-column h-100 ps-2" style="overflow:auto;">
              <div class="h-100 d-flex flex-column">
                <div style="flex:1; overflow:auto;">
                  {#if !webSocket}
                    <ResponseDefaultScreen />
                  {:else}
                    <div class="h-100 flex-column">
                      <div style="overflow:auto; height:50%;">
                        <ResponseData
                          {webSocket}
                          {onSearchMessage}
                          {onDeleteMessage}
                          {onUpdateMessageBody}
                          {onUpdateContentType}
                          {onUpdateFilterType}
                        />
                      </div>
                      <div style="overflow:auto; height:50%;">
                        <div class="h-100 d-flex flex-column">
                          <ResponsePreviewNavigator
                            {webSocket}
                            {isWebApp}
                            {onUpdateContentType}
                          />
                          <div class="pt-2"></div>
                          <div style="flex:1; overflow:auto;">
                            <ResponsePreview {webSocket} />
                          </div>
                        </div>
                      </div>
                    </div>
                  {/if}
                </div>
              </div>
            </div></Pane
          >
        </Splitpanes>
        <!-- {:else}
          <ResponseDefaultScreen isMainScreen={true} />
        {/if} -->
      </div>
    </div>
  </div>
  <Modal
    title={"Save Request"}
    type={"dark"}
    width={"55%"}
    zIndex={10000}
    isOpen={isExposeSaveAsSocket}
    handleModalState={(flag = false) => {
      isExposeSaveAsSocket = flag;
    }}
  >
    <SaveAsCollectionItem
      onClick={(flag = false) => {
        isExposeSaveAsSocket = flag;
      }}
      requestMethod={TabTypeEnum.WEB_SOCKET}
      requestUrl={$tab.property.websocket?.url}
      requestName={$tab.name}
      requestDescription={$tab.description}
      requestPath={$tab.path}
      collections={$collections}
      {readWorkspace}
      onSave={onSaveAsSocket}
      {onCreateFolder}
      {onCreateCollection}
      {onRenameCollection}
      {onRenameFolder}
    />
  </Modal>
{/if}

