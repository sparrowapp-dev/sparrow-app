<script lang="ts">
  import type { KeyValuePair } from "@sparrow/common/interfaces/request.interface";
  import { TabularInput } from "@sparrow/workspaces/components";
  import { createDeepCopy } from "@sparrow/common/utils/conversion.helper";
  import MixpanelEvent from "@app/utils/mixpanel/MixpanelEvent";
  import { Events } from "@sparrow/common/enums";
  import { Toggle } from "@sparrow/library/ui";
  import {
    addDynamicExpressionContent,
    updateIsCurrentExpression,
  } from "../../store";
  import {
    isDynamicExpressionContent,
    isDynamicExpressionModalOpen,
  } from "../../store";
  import { onMount } from "svelte";

  export let environmentVariables;
  export let selectedBlock;
  export let onHeadersChange;
  export let headers;
  export let autoGeneratedHeaders;
  export let authHeader;

  export let isBulkEditActive;
  let isBulkEditRequired = false;
  let isBulkEditHeaderInfoRequired = false;

  //   let placeholderHeader=`Usage: Use bulk edit to manage multiple Headers quickly. Separate each entry with a new line.
  // Format: Header-Key: Header-Value
  // Example: userID: Sparrow12`

  let placeholderHeader =
    "Usage - Manage multiple headers.  Format - Key: Value";

  let showGeneratedHeader: boolean = false;

  const handleHeaderChange = (pairs: KeyValuePair[]): void => {
    onHeadersChange("headers", pairs);
  };

  const handleAutoGeneratedChange = (pairs: KeyValuePair[]): void => {};

  const settingDynamicExpressionParams = (pairs: KeyValuePair[]) => {
    // Create a new array to avoid mutating the input directly
    let updatedParams: KeyValuePair[] = [...pairs];

    // Filter key values based on specific criteria
    const keyWithValues = $isDynamicExpressionContent.filter((item) => {
      return (
        item?.blockName === selectedBlock?.data?.blockName &&
        item?.requestType === "headers" &&
        item?.method === "request"
      );
    });

    // Iterate over the filtered dynamic expressions and update values
    for (const dynamicParam of keyWithValues) {
      if (dynamicParam.key.trim() !== "" && dynamicParam.value.trim() !== "") {
        const existingParam = updatedParams.find(
          (ele) => ele?.key.trim() === dynamicParam.key.trim(),
        );

        if (existingParam) {
          // Step 1: Remove any existing [[...]] parts from the value
          const cleanedValue = existingParam.value
            .replace(/\[\[.*?\]\]/g, "")
            .trim();

          // Step 2: Add the new dynamic value wrapped in [[...]]
          const newDynamicValue = `[[${dynamicParam.value}]]`;

          // Step 3: Update the existingParam's value with the cleaned value + new dynamic value
          existingParam.value = cleanedValue
            ? `${cleanedValue} ${newDynamicValue}`
            : newDynamicValue;
        }
      }
    }

    // Call the headers change callback with the updated params
    onHeadersChange("headers", updatedParams);
  };

  const toggleBulkEdit = (value: boolean) => {
    MixpanelEvent(Events.Bulk_Edit_Headers);
    onHeadersChange("isHeaderBulkEditActive", value);
  };

  const checkIfExists = (
    id: string,
    blockName: string,
    requestType: string,
    key: string,
    method: string,
    index: number,
  ) => {
    const items = $isDynamicExpressionContent;
    return items.some(
      (item) =>
        item.blockName === blockName &&
        item.requestType === requestType &&
        item.key === key &&
        item.method === method &&
        item.index === index &&
        item.id === id,
    );
  };

  function generateUniqueId() {
    return "_" + Math.random().toString(36).substr(2, 9);
  }

  const handleDynamicExpression = (key: string, index: number, id: string) => {
    if (
      checkIfExists(
        id,
        selectedBlock?.data?.blockName,
        "headers",
        key,
        "request",
        index,
      )
    ) {
      updateIsCurrentExpression(
        id,
        selectedBlock?.data?.blockName,
        "headers",
        "request",
        key,
        index,
      );
      return;
    }
    addDynamicExpressionContent(
      generateUniqueId(),
      selectedBlock?.data?.blockName,
      "headers",
      key,
      "",
      "request",
      index,
      true,
    );
  };

  const handleRemoveDynamicExpression = (
    key: string,
    index: number,
    id: string,
  ) => {
    const updatedItems = $isDynamicExpressionContent.filter(
      (item) =>
        !(
          item?.blockName === selectedBlock?.data?.blockName &&
          item?.requestType === "headers" &&
          item?.method === "request" &&
          item?.key === key &&
          item?.index === index &&
          item?.id == id
        ),
    );

    isDynamicExpressionContent.set(updatedItems);
  };

  const handleDynamicNewExpression = (key: string, index: number) => {
    addDynamicExpressionContent(
      generateUniqueId(),
      selectedBlock?.data?.blockName,
      "headers",
      key,
      "",
      "request",
      index,
      true,
    );
  };

  const handleRemoveDynamicExpressionKey = (key: string, index: number) => {
    const updatedItems = $isDynamicExpressionContent.filter(
      (item) =>
        !(
          item?.blockName === selectedBlock?.data?.blockName &&
          item?.requestType === "headers" &&
          item?.method === "request" &&
          item?.key === key &&
          item?.index === index
        ),
    );

    isDynamicExpressionContent.set(updatedItems);
  };

  const handleOpenCurrentDynamicExpression = (
    key: string,
    index: number,
    id: string,
  ) => {
    const itemIndex = $isDynamicExpressionContent.findIndex(
      (item) =>
        item?.id === id &&
        item?.blockName === selectedBlock?.data?.blockName &&
        item?.requestType === "headers" &&
        item?.method === "request" &&
        item?.key === key &&
        item?.index === index,
    );
    if (itemIndex !== -1) {
      const updatedItem = {
        ...$isDynamicExpressionContent[itemIndex],
        isCurrentOpen: true,
      };

      // Create a new array with the updated item
      $isDynamicExpressionContent = [
        ...$isDynamicExpressionContent.slice(0, itemIndex),
        updatedItem,
        ...$isDynamicExpressionContent.slice(itemIndex + 1),
      ];

      console.log("Updated item:", updatedItem);
    }
    $isDynamicExpressionModalOpen = true;
  };

  let DynamicExpressionParams;

  $: {
    const paramsWithDE = $isDynamicExpressionContent.filter(
      (item) =>
        item?.blockName === selectedBlock?.data?.blockName &&
        item?.requestType === "headers" &&
        item?.method === "request",
    );
    // Now create the final params array
    DynamicExpressionParams = headers.map((param: any) => {
      const matchingDE = paramsWithDE.filter(
        (deItem) => deItem.key === param.key,
      );
      return {
        ...param,
        DE: matchingDE,
      };
    });
  }

  const getDEByKeyAndValue = (
    key: string,
    value: string,
    index: number,
  ): any => {
    const data = $isDynamicExpressionContent?.filter(
      (item: any) =>
        item?.requestType === "queryParams" &&
        item?.method === "request" &&
        item?.blockName === "Block 1" &&
        item?.key === key &&
        item?.index === index,
    );
    return data;
  };

  isDynamicExpressionContent.subscribe(() => {
    settingDynamicExpressionParams(headers);
  });

  onMount(() => {
    settingDynamicExpressionParams(headers);
  });
</script>

<section class="w-100" style="">
  <TabularInput
    isCheckBoxEditable={true}
    isInputBoxEditable={true}
    {isBulkEditRequired}
    bulkEditPlaceholder={placeholderHeader}
    {isBulkEditHeaderInfoRequired}
    {isBulkEditActive}
    onToggleBulkEdit={toggleBulkEdit}
    keyValue={createDeepCopy(headers)}
    callback={handleHeaderChange}
    {environmentVariables}
    onUpdateEnvironment={() => {}}
    dynamicExpression={true}
    {getDEByKeyAndValue}
    {handleDynamicExpression}
    {handleOpenCurrentDynamicExpression}
    {handleRemoveDynamicExpression}
    {handleDynamicNewExpression}
    {handleRemoveDynamicExpressionKey}
  />
  <div>
    {#if !isBulkEditActive}
      <div class="d-flex align-items-center py-3">
        <Toggle
          bind:isActive={showGeneratedHeader}
          label="Show auto-generated headers"
        />
      </div>
    {/if}
  </div>

  {#if showGeneratedHeader && !isBulkEditActive}
    <TabularInput
      isCheckBoxEditable={false}
      isInputBoxEditable={false}
      isTopHeaderRequired={false}
      readable={authHeader}
      keyValue={createDeepCopy(autoGeneratedHeaders)}
      callback={handleAutoGeneratedChange}
      {environmentVariables}
      onToggleBulkEdit={() => {}}
      onUpdateEnvironment={() => {}}
      {handleRemoveDynamicExpression}
    />
  {/if}
</section>

