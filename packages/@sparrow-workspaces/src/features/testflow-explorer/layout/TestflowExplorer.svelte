        text: [
          {
            key: "",
            value: "",
            checked: false,
          },
        ],
        file: [],
      };
    }
    if (data?.request?.auth) {
      response.auth = data?.request?.auth;
    } else {
      response.auth = tempTab?.auth;
    }
    if (data?.request?.url) {
      response.url = data?.request?.url;
    } else {
      response.url = tempTab?.url;
