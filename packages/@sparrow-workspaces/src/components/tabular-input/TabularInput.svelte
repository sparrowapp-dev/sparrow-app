  // ********** Diff/Merge View Methods - Start **********

  // Calculate diff between original and current data **anish
  type DiffType = "added" | "deleted" | "modified" | "unchanged";
  type DiffPair = KeyValuePair & {
    diffType: DiffType;
    originalIndex?: number;
    currentIndex?: number;
