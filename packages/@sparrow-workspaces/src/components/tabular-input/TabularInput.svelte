              {#if isBulkEditRequired}
                <Toggle
                  bind:isActive={bulkToggle}
                  label="Bulk Edit"
                  fontSize="12px"
                  textColor="var(--text-ds-neutral-200)"
                  fontWeight="400"
                  onClick={handleBulkTextUpdate}
                  onChange={toggleBulkEdit}
                />
              {/if}
              <!-- <Toggle
                isActive={showMergeView}
                label="Show Diff"
                fontSize="12px"
